# Test: Content Patterns Match Profile
# ═══════════════════════════════════════════════════════════════════════════════


class TestContentPatterns:
    """Verify content formatting patterns match profile."""

    def test_claim_numbering(self, generated_doc, profile):
        """Claims use correct numbering separator."""
        sep = profile.content_patterns.claim_numbering_separator
        for para in generated_doc.paragraphs:
            text = para.text.strip()
            if text.startswith("1" + sep):
                assert True
                return
        pytest.fail("No claim with correct numbering found")

    def test_dependent_claim_format(self, generated_doc, profile):
        """Dependent claims use format from profile."""
        dep_marker = "根据权利要求"
        for para in generated_doc.paragraphs:
            text = para.text.strip()
            if dep_marker in text:
                assert True
                return
        pytest.fail("No dependent claim found")
