# Test: Paragraph Format Matches Profile
# ═══════════════════════════════════════════════════════════════════════════════


class TestParagraphFormat:
    """Verify paragraph formatting matches profile."""

    def test_first_line_indent(self, generated_doc, profile):
        """Body text first line indent matches profile."""
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style or not para.text.strip():
                continue
            fmt = para.paragraph_format
            if fmt.first_line_indent:
                actual = fmt.first_line_indent.cm
                expected = profile.paragraph_format.body_first_line_indent_cm
                assert actual == pytest.approx(expected, abs=0.05)
                break

    def test_line_spacing(self, generated_doc, profile):
        """Body text line spacing matches profile (in twips via XML)."""
        expected_twips = round(profile.paragraph_format.body_line_spacing_emu / 635)

        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style or not para.text.strip():
                continue
            pPr = para._element.find(qn("w:pPr"))
            if pPr is not None:
                spacing = pPr.find(qn("w:spacing"))
                if spacing is not None:
                    line_val = spacing.get(qn("w:line"))
                    line_rule = spacing.get(qn("w:lineRule"))
                    if line_val and line_rule == "exact":
                        assert int(line_val) == expected_twips
                        return

        pytest.fail("No paragraph with exact line spacing found")

    def test_body_alignment_justify(self, generated_doc, profile):
        """Body text alignment matches profile."""
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style or not para.text.strip():
                continue
            if para.paragraph_format.first_line_indent:
                # This is a body paragraph
                assert para.paragraph_format.alignment == pytest.approx(3)  # JUSTIFY = 3
                break

    def test_heading_alignment_center(self, generated_doc, profile):
        """Document headings are centered."""
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style and para.text.strip():
                assert para.alignment == pytest.approx(1)  # CENTER = 1
                break


