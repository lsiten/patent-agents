class TestPageLayout:
    """Verify generated document page layout matches profile."""

    def test_page_size(self, generated_doc, profile):
        """Page size matches profile (A4)."""
        section = generated_doc.sections[0]
        assert section.page_width.cm == pytest.approx(profile.page_layout.page_width_cm, abs=0.1)
        assert section.page_height.cm == pytest.approx(profile.page_layout.page_height_cm, abs=0.1)

    def test_five_sections(self, generated_doc, profile):
        """Document has correct number of sections."""
        assert len(generated_doc.sections) == profile.document_structure.num_sections

    def test_abstract_margins(self, generated_doc, profile):
        """摘要 section margins match profile."""
        s = generated_doc.sections[0]
        expected = profile.page_layout.margins_abstract
        assert s.top_margin.cm == pytest.approx(expected[0], abs=0.05)
        assert s.bottom_margin.cm == pytest.approx(expected[1], abs=0.05)
        assert s.left_margin.cm == pytest.approx(expected[2], abs=0.05)
        assert s.right_margin.cm == pytest.approx(expected[3], abs=0.05)

