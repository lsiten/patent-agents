# Test: Typography Matches Profile
# ═══════════════════════════════════════════════════════════════════════════════


class TestTypography:
    """Verify generated fonts/sizes match profile."""

    def test_body_font_name(self, generated_doc, profile):
        """Body text font matches profile."""
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style or not para.text.strip() or not para.runs:
                continue
            run = para.runs[0]
            assert run.font.name == profile.typography.body_font_name
            break

    def test_body_font_size(self, generated_doc, profile):
        """Body text size matches profile."""
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style or not para.text.strip() or not para.runs:
                continue
            run = para.runs[0]
            assert run.font.size.pt == pytest.approx(
                profile.typography.body_font_size_pt, abs=0.5
            )
            break

    def test_body_font_east_asian(self, generated_doc, profile):
        """East-Asian font attribute set correctly."""
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style or not para.text.strip() or not para.runs:
                continue
            run = para.runs[0]
            rPr = run._element.find(qn("w:rPr"))
            rFonts = rPr.find(qn("w:rFonts")) if rPr is not None else None
            if rFonts is not None:
                ea = rFonts.get(qn("w:eastAsia"))
                assert ea == profile.typography.body_font_name
            break

    def test_heading_font(self, generated_doc, profile):
        """Document heading font matches profile."""
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style and para.text.strip() and para.runs:
                run = para.runs[0]
                assert run.font.name == profile.typography.doc_heading_font_name
                break


