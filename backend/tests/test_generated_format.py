# Test: Document Structure Matches Profile
# ═══════════════════════════════════════════════════════════════════════════════


class TestDocumentStructure:
    """Verify document structure matches profile."""

    def test_section_count(self, generated_doc, profile):
        """Correct number of document sections."""
        assert len(generated_doc.sections) == profile.document_structure.num_sections

    def test_heading_char_spacing(self, generated_doc, profile):
        """Document headings have spaced characters."""
        sep = profile.document_structure.heading_char_separator
        for para in generated_doc.paragraphs:
            style = para.style.name if para.style else ""
            if "Heading" in style and para.text.strip():
                # Verify characters are separated
                assert sep in para.text
                break

    def test_description_subsections_present(self, generated_doc, profile):
        """All expected description sub-sections are present."""
        expected = set(profile.document_structure.description_sections)
        found = set()

        for para in generated_doc.paragraphs:
            text = para.text.strip()
            if text in expected and para.runs and para.runs[0].bold:
                found.add(text)

        # At minimum: 技术领域, 背景技术, 发明内容, 具体实施方式
        required = {"技术领域", "背景技术", "发明内容", "具体实施方式"}
        assert required.issubset(found), f"Missing: {required - found}"


